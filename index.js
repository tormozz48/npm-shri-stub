'use strict';

/**
 * Calculates sum of two given numbers
 * @param {Number} a
 * @param {Number} b
 */
exports.add = (a, b) => a + b;

/**
 * Calculates difference between two given numbers
 * @param {Number} a
 * @param {Number} b
 */
exports.subtract = (a, b) => a - b;

/**
 * Multiplies given numbers
 * @param {Number} a
 * @param {Number} b
 */
exports.multiply = (a, b) => a * b;

/**
 * Divides given numbers
 * @param {Number} a
 * @param {Number} b
 */
exports.divide = (a, b) => a / b;
